/*
   String Functions in JavaScript
   How to Create Object in String
*/
// Diffrent ways of creation of String Object
//First Approach [String literals]
let x="Welcome";
console.log(x);

//Second Approach [using new operator and String class]
let y=new String("Good Afternoon")
console.log(y.toString());

//Lowercase function
let s1="PROGRAM"
let s2=s1.toLowerCase()
console.log("Lowercase Result :"+s2);

//uppercase function
let s3=new String("language")
console.log("Uppercase Result :"+s3.toUpperCase());

// Number of characters in String
let s4=new String("good morning")
console.log("# of Characters :"+s4.length);





