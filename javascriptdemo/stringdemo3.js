/*
   String Functions in JavaScript
*/
// toString function
let x=new String("S G Software")
console.log("Convert to String :"+x.toString());

// concatenation of Strings
let a1="S G Software"
let a2="Testing Institute"
console.log("concatenation Result:"+(a1+" "+a2))
console.log("Concatenation Result:"+ a1.concat(a2));

// Repeat Function
let b="Good Morning"
console.log("Repeat :"+b.repeat(3))

//Replace Function
let c="It is a old palace"
console.log("Replaced Result :"+c.replace("is","was"));

